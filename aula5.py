# Tipo de dado bool (boolean)
# Ao questionar algo em um programa,
# Só existe duas resposta possivel:
# Sim (true) ou Não (false).
# Existem varios operadores para "questionar".
# Dentro deles o ==, que é um operador logico que 
# questiona  se um valor e igual a outro 

print(10 == 10) #Sim => True (Verdadeiro)
print(10 == 12) #Não => False (Falso)
print(type(True))
print(type(False))
print(type(10 == 10))
print(type(10 == 12))
